export function useOrgName(): string {
    const org = useResource(OrganizationResource.detail(), {
        name: groupToOrg(
            localStorage?.getItem(GLOBAL_STORAGE_KEYS.GLOBAL_ORG) || undefined
        ),
    });
    return org?.description || "";
}
